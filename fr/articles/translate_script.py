def extract_p_tags(content):
    """Extract all <p> tags and their content from HTML."""
    # Match <p> tags with content, handling attributes
    pattern = r'<p[^>]*>(.*?)</p>'
    matches = re.findall(pattern, content, re.DOTALL)
    return matches

